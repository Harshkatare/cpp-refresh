#include <iostream>
using namespace std;

int main()
{
    // var.. declaration and initialization

    int a = 30;
    int b = 10;

    // var.. reassignment

    a = a + 2;

    // using vars in expressions

    int sum = a + b;
    int product = a * b;

        // outputs

    cout << "Value of a: " << a << endl;
    cout << "Value of b: " << b << endl;
    cout << "Sum: " << sum << endl;
    cout << "Product: " << product << endl;

    return 0;
}